# целые яисла с минимальной шириной

print("{:20d}".format(3))

# ширина не работает для чисел длинее заполнения

print("{:2d}".format(333))

# заполнение для чисел с плавующей точкой

print("{:7.4f}".format(3.14))

# целые числа с минимальной шириной, заполненные нулями

print("{:03d}".format(7))

# заполнение для чисел с плавующей запятой, заполненных нулями

print("{:08.3f}".format(12.2346))
