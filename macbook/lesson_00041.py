# стоит упомянуть, что существует функция format(),
# которая очень близка по своему поведению к методу
# строки .format().
# Пара небольших примеров из официальной докусентации:

print(format(1, 'f'))
print(format(1000, '#>+10,.2f'))
print(format(1000.5368, '~>+15,.2f'))
print(format('format', '.2'))

