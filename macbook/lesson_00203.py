# Создаём строку
my_str = 'Сидел барсук в своей норе и ел картошечку пюре'
print(my_str)

# Получаем длину строки с помощью встроенного меотда len()
print(len(my_str))

# Выполняем конкатенацию строк
print(my_str + '.')

# Определяем вхождение подстроки 'ре'
print('ре' in my_str)

# Определяем количество вхождений подстроки 'ре'
print(my_str.count('ре'))

